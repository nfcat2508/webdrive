  defp local_lib(name) do
    path_file = Path.expand("./.#{Atom.to_string(name)}_path", __DIR__)

    if File.exists?(path_file) do
      path = path_file |> File.read!() |> String.trim()
      {name, path: Path.expand(path, __DIR__)}
    else
      raise """
      The file '#{path_file}' must be present containing the path to the #{name} library
      """
    end
  end

